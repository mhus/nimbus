package de.mhus.nimbus.worldlife.entity;

/**
 * Enum representing different types of world characters
 */
public enum CharacterType {
    /**
     * Player character controlled by a human player
     */
    PLAYER,

    /**
     * Non-player character (AI controlled)
     */
    NPC,

    /**
     * Flora entities (plants, trees, etc.)
     */
    FLORA,

    /**
     * Fauna entities (animals, creatures, etc.)
     */
    FAUNA
}
